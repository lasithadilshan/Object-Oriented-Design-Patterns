    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedItem().toString().equals("Credite Card")) {
            setStrategy(creditCardStrategy);
            strategy.Show(true);

            setStrategy(ezCashStrategy);
            strategy.Show(false);

        } else if (jComboBox1.getSelectedItem().toString().equals("Easy Cash")) {
            setStrategy(creditCardStrategy);
            strategy.Show(false);

            setStrategy(ezCashStrategy);
            strategy.Show(true);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

